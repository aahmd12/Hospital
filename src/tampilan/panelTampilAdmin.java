   public void loadDataAdmin() {

    DefaultTableModel model = (DefaultTableModel) tableAdmin.getModel();
    model.setRowCount(0);

    String sql = "SELECT id_admin, nama, email FROM admin";

    Koneksi konek = new Koneksi();
    Connection conn = konek.connect();

    try (PreparedStatement ps = conn.prepareStatement(sql);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getString("id_admin"),
                rs.getString("nama"),
                rs.getString("email")
            });

        }

    } catch (SQLException ex) {

        JOptionPane.showMessageDialog(this,
                "Gagal memuat data : " + ex.getMessage());

    }
}

