    private void tampilkanData() {
        Object[] Baris = {"ID Pasien", "Nama Pasien", "Jenis Kelamin", "No HP", "Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        
        // Pengecekan kalau txtCari belum dibuat, dikasih string kosong aja
        String cariitem = ""; 
        if (txtCari != null) {
            cariitem = txtCari.getText();
        }

        try {
            String sql = "SELECT * FROM pasien WHERE id_pasien LIKE '%" + cariitem + "%' "
                    + "OR nama_pasien LIKE '%" + cariitem + "%'";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("id_pasien"),
                    hasil.getString("nama_pasien"),
                    hasil.getString("jenis_kelamin"),
                    hasil.getString("no_hp"),
                    hasil.getString("alamat")
                });
            }
            tabelPasien.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e);
        }
    }

