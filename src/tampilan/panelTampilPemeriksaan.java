    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed

        try {

            if (conn == null || conn.isClosed()) {
                conn = new Koneksi().connect();
            }

            JasperReport jr = JasperCompileManager.compileReport(
                getClass().getResourceAsStream("/ireport/pemeriksaan.jrxml")
            );

            Map<String, Object> param = new HashMap<>();
            JasperPrint jp = JasperFillManager.fillReport(jr, param, conn);
            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal mencetak laporan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCetakActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

       int baris = tblPeriksa.getSelectedRow();

        if (baris == -1) {
            JOptionPane.showMessageDialog(this,
                "Pilih data terlebih dahulu!");
            return;
        }

        String idPeriksa = tblPeriksa.getValueAt(baris, 0).toString();

        dashboard.showPanel(new panelEditPemeriksaan(dashboard, idPeriksa));

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed

      if (idPeriksa.equals("")) {

    JOptionPane.showMessageDialog(null,
            "Pilih data terlebih dahulu!");

    return;
}

int jawab = JOptionPane.showConfirmDialog(
        null,
        "Hapus data ini?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION);

if (jawab == JOptionPane.YES_OPTION) {

    try {

        String sql = "DELETE FROM periksa WHERE id_periksa=?";

        pst = conn.prepareStatement(sql);

        pst.setString(1, idPeriksa);
     

        pst.executeUpdate();

        JOptionPane.showMessageDialog(null,
                "Data berhasil dihapus");

        tampilData();

        idPeriksa = "";

    } catch(Exception e){
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, e.getMessage());

    }
}
    }//GEN-LAST:event_btnHapusActionPerformed

