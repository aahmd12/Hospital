    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        int bar = tabelobat.getSelectedRow();
        if (bar == -1) {
            JOptionPane.showMessageDialog(null, "pilih data obat yang ingin diubah");
            return;
        }
        String kdobat = tabmode.getValueAt(bar, 0).toString();
        String nmobat = tabmode.getValueAt(bar, 1).toString();
        String kategori = tabmode.getValueAt(bar, 2).toString();
        String satuan = tabmode.getValueAt(bar, 3).toString();
        String harga = tabmode.getValueAt(bar, 4).toString();
        String stok = tabmode.getValueAt(bar, 5).toString();
        panelEditObat panel = new panelEditObat(dashboard);
        panel.setObat(kdobat, nmobat, kategori, satuan, harga, stok);
        dashboard.showPanel(panel);
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        int bar = tabelobat.getSelectedRow();
        if (bar == -1) {
            JOptionPane.showMessageDialog(null, "pilih data obat yang ingin dihapus");
            return;
        }
        String kdobat = tabmode.getValueAt(bar, 0).toString();
        int ok = JOptionPane.showConfirmDialog(null, "hapus", "konfirmasi dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from obat where kd_obat='" + kdobat + "'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
                datatable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal dihapus" + e);
            }
        }
    }//GEN-LAST:event_bhapusActionPerformed

