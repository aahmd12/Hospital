    private void tampilData(){

     DefaultTableModel model = new DefaultTableModel();

    model.addColumn("No Pemeriksaan");
    model.addColumn("Tanggal");
    model.addColumn("No Pasien");
    model.addColumn("Nama Pasien");
    model.addColumn("Nama Dokter");
    model.addColumn("Diagnosis");
    model.addColumn("Tindakan");

    try{

        String sql =
        "SELECT periksa.id_periksa, periksa.tgl_periksa, " +
        "pasien.id_pasien, pasien.nama_pasien, " +
        "dokter.nama_dokter, periksa.diagnosa, periksa.tindakan " +
        "FROM periksa " +
        "JOIN pasien ON pasien.id_pasien = periksa.id_pasien " +
        "JOIN dokter ON dokter.id_dokter = periksa.id_dokter";

        pst = conn.prepareStatement(sql);

        rs = pst.executeQuery();

        while(rs.next()){

            model.addRow(new Object[]{
                rs.getString("id_periksa"),
                rs.getString("tgl_periksa"),
                rs.getString("id_pasien"),
                rs.getString("nama_pasien"),
                rs.getString("nama_dokter"),
                rs.getString("diagnosa"),
                rs.getString("tindakan")
            });

        }

        tblPeriksa.setModel(model);

    }catch(Exception e){
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, e.getMessage());

    }

}

