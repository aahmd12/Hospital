    protected void datatable(){
        
        Object[] Baris = {"ID Perawat","Nama Perawat","Jenis Kelamin","No. Telepon","Spesialis","Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        
        String nama = tnmperawat.getText().trim();
        String spesialis = cbspesialis.getSelectedItem().toString();
        
        String sql = "SELECT * FROM perawat WHERE 1=1";
        if(!nama.equals("")){
            sql += " AND nama_perawat LIKE '%"+nama+"%'";
        }
        
        if(!spesialis.equals("Semua Spesialis")){
            sql += " AND spesialis='"+spesialis+"'";
        }
        
        sql += " ORDER BY id_perawat ASC";

        try{
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                    hasil.getString("id_perawat"),
                    hasil.getString("nama_perawat"),
                    hasil.getString("jenis_kelamin"),
                    hasil.getString("no_hp"),
                    hasil.getString("spesialis"),
                    hasil.getString("alamat")
                });
            }
            
            tabelperawat.setModel(tabmode);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }


