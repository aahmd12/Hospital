    public void setRuangan(String kdruangan, String nmruangan, String jenis, String lantai, String kapasitas, String fasilitas, String status) {
        this.kdruangan.setText(kdruangan);
        this.nmruangan.setText(nmruangan);
        this.jenis.setSelectedItem(jenis);
        this.lantai.setValue(Integer.parseInt(lantai));
        this.kapasitas.setValue(Integer.parseInt(kapasitas));
        this.fasilitas.setText(fasilitas);
        if (status.equals("Tersedia")) {
            tersedia.setSelected(true);
        } else if (status.equals("Sedang digunakan")) {
            digunakan.setSelected(true);
        }

        this.kdruangan.setEditable(false);
    }

