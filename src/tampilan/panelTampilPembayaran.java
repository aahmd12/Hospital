    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // SIMPAN DATA KE DATABASE
String idPasien = txtidpasien.getText().trim();
String namaPasien = txtnmpasien.getText().trim();
String metode = txtmtdbayar.getSelectedItem().toString();
String jumlah = txtjmlbayar.getText().trim();
Date tanggal = (Date) jSpinner1.getValue();

if (idPasien.equals("") || jumlah.equals("")) {
    JOptionPane.showMessageDialog(null, "Data belum lengkap!");
    return;
}

try {
    String sql = "INSERT INTO payment "
            + "(id_pasien, nama_pasien, metode_pembayaran, jumlah_bayar, tanggal_pembayaran) "
            + "VALUES (?, ?, ?, ?, ?)";

    pst = conn.prepareStatement(sql);

    pst.setString(1, idPasien);
    pst.setString(2, namaPasien);
    pst.setString(3, metode);
    pst.setDouble(4, Double.parseDouble(jumlah));
    pst.setTimestamp(5, new java.sql.Timestamp(tanggal.getTime()));

    pst.executeUpdate();

    JOptionPane.showMessageDialog(null, "Data pembayaran berhasil disimpan");

    loadDataBayar();
    clearForm();

} catch (Exception e) {
    JOptionPane.showMessageDialog(null, e);
}
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // HAPUS DATA YANG DIPILIH PADA TABEL
       if(idPayment.equals("")){

        JOptionPane.showMessageDialog(null,
                "Pilih data terlebih dahulu!");

        return;
    }

    int jawab = JOptionPane.showConfirmDialog(
            null,
            "Hapus data pembayaran ini?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

    if(jawab == JOptionPane.YES_OPTION){

        try{

            String sql = "DELETE FROM payment WHERE id_payment=?";

            pst = conn.prepareStatement(sql);

            pst.setString(1, idPayment);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null,
                    "Data berhasil dihapus");

            loadDataBayar();

            clearForm();

        }catch(Exception e){

            JOptionPane.showMessageDialog(null,e);

        }

    }

    }//GEN-LAST:event_bhapusActionPerformed

    private void bcetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcetakActionPerformed
        // CETAK: Cetak single data terpilih di jTable1 menggunakan iReport
       if(idPayment.equals("")){

        JOptionPane.showMessageDialog(null,
                "Pilih data terlebih dahulu!");

        return;
    }

    try{

        InputStream report = getClass().getResourceAsStream("/ireport/payment.jrxml");

        if(report == null){
            JOptionPane.showMessageDialog(null,
                    "File payment.jrxml tidak ditemukan!");
            return;
        }

        JasperReport jr = JasperCompileManager.compileReport(report);

        Map<String,Object> param = new HashMap<>();

        param.put("id_payment", Integer.parseInt(idPayment));

        JasperPrint jp = JasperFillManager.fillReport(
                jr,
                param,
                conn);

        JasperViewer.viewReport(jp,false);

    }catch (Exception e) {
    e.printStackTrace();

    Throwable t = e;
    while (t != null) {
        System.out.println("CAUSE = " + t);
        t = t.getCause();
    }
    
    }


    }//GEN-LAST:event_bcetakActionPerformed


