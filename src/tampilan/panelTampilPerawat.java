    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        int baris = tabelperawat.getSelectedRow();

        if(baris == -1){
            JOptionPane.showMessageDialog(null,"Pilih data yang akan diubah!");
            return;
        }

        panelEditPerawat panel = new panelEditPerawat(dashboard);
        panel.setData(
            tabmode.getValueAt(baris,0).toString(),
            tabmode.getValueAt(baris,1).toString(),
            tabmode.getValueAt(baris,2).toString(),
            tabmode.getValueAt(baris,3).toString(),
            tabmode.getValueAt(baris,4).toString(),
            tabmode.getValueAt(baris,5).toString()
        );

        dashboard.showPanel(panel);
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        int bar = tabelperawat.getSelectedRow();
        if (bar == -1) {
            JOptionPane.showMessageDialog(null, "pilih data perawat yang ingin dihapus");
            return;
        }
        String id = tabmode.getValueAt(bar, 0).toString();
        int ok = JOptionPane.showConfirmDialog(null, "hapus data perawat " + id + " ?", "konfirmasi dialog",
            JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from perawat where id_perawat = ?";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, id);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal dihapus" + e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapusActionPerformed


