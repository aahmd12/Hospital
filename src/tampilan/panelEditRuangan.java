    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE ruangan SET nama_ruangan=?, jenis=?, lantai=?, kapasitas=?, fasilitas=?, status=? WHERE kd_ruangan=?";
        try (PreparedStatement stat = conn.prepareStatement(sql)) {
            stat.setString(1, nmruangan.getText());
            stat.setString(2, (String) jenis.getSelectedItem());
            stat.setInt(3, (Integer) lantai.getValue());
            stat.setInt(4, (Integer) kapasitas.getValue());
            stat.setString(5, fasilitas.getText());
            String status = tersedia.isSelected() ? "Tersedia" : "Sedang digunakan";
            stat.setString(6, status);
            stat.setString(7, kdruangan.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            dashboard.showPanel(new panelTampilRuangan(dashboard));
        } catch (SQLException e) {
          
            JOptionPane.showMessageDialog(null, "Data gagal diubah: " + e.getMessage());
        }
    }//GEN-LAST:event_bubahActionPerformed


