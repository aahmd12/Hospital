     protected void datatable() {
        Object[] Baris = {"Kd Obat", "Nama Obat", "Kategori", "Satuan", "Harga", "Stok"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelobat.setModel(tabmode);
        String sql = "select * from obat where 1=1";
        if (!kdobat.getText().isEmpty()) {
            sql += " and kd_obat like '%" + kdobat.getText() + "%'";
        }
        if (!nmobat.getText().isEmpty()) {
            sql += " and nm_obat like '%" + nmobat.getText() + "%'";
        }
        if (kategori.getSelectedIndex() > 0) {
            sql += " and kategori = '" + kategori.getSelectedItem().toString() + "'";
        }
        if (!stok.getText().isEmpty()) {
            sql += " and stok = '" + stok.getText() + "'";
        }
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("kd_obat"),
                    hasil.getString("nm_obat"),
                    hasil.getString("kategori"),
                    hasil.getString("satuan"),
                    hasil.getString("harga"),
                    hasil.getString("stok")
                });
            }
            tabelobat.setModel(tabmode);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil" + e);
        }
    }

