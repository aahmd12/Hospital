    protected void datatable(){
        
        Object[] Baris = {"ID Dokter","Nama Dokter","No.Telepon","Spesialis","Alamat","Jenis"};
        tabmode = new DefaultTableModel(null, Baris);
        
        String nama = tnmdokter.getText().trim();
        String spesialis = cbspesialis.getSelectedItem().toString();
        
        String sql = "SELECT * FROM dokter WHERE 1=1";
        if(!nama.equals("")){
            sql += " AND nama_dokter LIKE '%"+nama+"%'";
        }
        
        if(!spesialis.equals("Semua Spesialis")){
            sql += " AND spesialis='"+spesialis+"'";
        }
        
        sql += " ORDER BY id_dokter ASC";

        try{
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                    hasil.getString("id_dokter"),
                    hasil.getString("nama_dokter"),
                    hasil.getString("no_hp"),
                    hasil.getString("spesialis"),
                    hasil.getString("alamat"),
                    hasil.getString("jenis_kelamin")
                });
            }
            
            tabeldokter.setModel(tabmode);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }

