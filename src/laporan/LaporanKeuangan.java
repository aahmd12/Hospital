    private void bcetak1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcetak1ActionPerformed
        try {
            InputStream report = getClass().getResourceAsStream("/ireport/LaporanKeuangan.jasper");

            if (report == null) {
                JOptionPane.showMessageDialog(null, "File LaporanObat.jasper tidak ditemukan!");
                return;
            }

            HashMap<String, Object> parameters = new HashMap<>();

            JasperPrint print = JasperFillManager.fillReport(report, parameters, conn);

            if (print.getPages().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Report kosong. Data tidak ditemukan dari database.");
                return;
            }

            JasperViewer viewer = new JasperViewer(print, false);
            viewer.setVisible(true);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal menampilkan report:\n" + e.getMessage());
        }
    }//GEN-LAST:event_bcetak1ActionPerformed


