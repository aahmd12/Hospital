    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String jk = "";
        if (rbLaki.isSelected()) {
            jk = "Laki-laki";
        } else if (rbPerempuan.isSelected()) {
            jk = "Perempuan";
        } else {
            JOptionPane.showMessageDialog(null, "Pilih jenis kelamin dulu!");
            return;
        }

        try {
            String sql = "INSERT INTO pasien (id_pasien, nama_pasien, jenis_kelamin, no_hp, alamat) VALUES (?,?,?,?,?)";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtIdPasien.getText());
            stat.setString(2, txtNamaPasien.getText());
            stat.setString(3, jk);
            stat.setString(4, txtNoHp.getText());
            stat.setString(5, txtAlamat.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");
            kosongkanForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal disimpan: " + e);
        }
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

