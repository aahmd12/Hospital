    private void loadDataKeuangan() {
    DefaultTableModel model = new DefaultTableModel(
        new Object[]{"No", "Nama Pasien", "Metode Pembayaran", "Jumlah Pembayaran", "Tanggal"},
        0
    ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };

    String metode = jenis.getSelectedItem() != null
            ? jenis.getSelectedItem().toString()
            : "SEMUA";

    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

    StringBuilder sql = new StringBuilder();
    sql.append("SELECT ")
       .append("py.id_payment, ")
       .append("ps.nama_pasien, ")
       .append("py.metode_pembayaran, ")
       .append("py.jumlah_bayar, ")
       .append("py.tanggal_pembayaran ")
       .append("FROM payment py ")
       .append("INNER JOIN pasien ps ON py.id_pasien = ps.id_pasien ")
       .append("WHERE 1=1 ");

    if (!metode.equalsIgnoreCase("SEMUA")) {
        sql.append("AND py.metode_pembayaran = ? ");
    }

    if (tanggal1.getDate() != null && tanggal2.getDate() != null) {
        sql.append("AND DATE(py.tanggal_pembayaran) BETWEEN ? AND ? ");
    } else if (tanggal1.getDate() != null) {
        sql.append("AND DATE(py.tanggal_pembayaran) = ? ");
    } else if (tanggal2.getDate() != null) {
        sql.append("AND DATE(py.tanggal_pembayaran) = ? ");
    }

    sql.append("ORDER BY py.tanggal_pembayaran DESC");

    try {
        if (conn == null || conn.isClosed()) {
            conn = new Koneksi().connect();
        }

        PreparedStatement pst = conn.prepareStatement(sql.toString());

        int index = 1;

        if (!metode.equalsIgnoreCase("SEMUA")) {
            pst.setString(index++, metode);
        }

        if (tanggal1.getDate() != null && tanggal2.getDate() != null) {
            String tanggalAwal = sdf.format(tanggal1.getDate());
            String tanggalAkhir = sdf.format(tanggal2.getDate());

            pst.setString(index++, tanggalAwal);
            pst.setString(index++, tanggalAkhir);

        } else if (tanggal1.getDate() != null) {
            String tanggalAwal = sdf.format(tanggal1.getDate());
            pst.setString(index++, tanggalAwal);

        } else if (tanggal2.getDate() != null) {
            String tanggalAkhir = sdf.format(tanggal2.getDate());
            pst.setString(index++, tanggalAkhir);
        }

        ResultSet rs = pst.executeQuery();

        int no = 1;
        int totalTransaksi = 0;
        BigDecimal totalNominal = BigDecimal.ZERO;

        while (rs.next()) {
            BigDecimal jumlahBayar = rs.getBigDecimal("jumlah_bayar");

            if (jumlahBayar == null) {
                jumlahBayar = BigDecimal.ZERO;
            }

            model.addRow(new Object[]{
                no++,
                rs.getString("nama_pasien"),
                rs.getString("metode_pembayaran"),
                jumlahBayar,
                rs.getTimestamp("tanggal_pembayaran")
            });

            totalTransaksi++;
            totalNominal = totalNominal.add(jumlahBayar);
        }

        tableobat1.setModel(model);

        NumberFormat rupiahFormat = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));

        ltransaksi.setText(String.valueOf(totalTransaksi));
        lnominal.setText(rupiahFormat.format(totalNominal));

        rs.close();
        pst.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(
            this,
            "Gagal memfilter data keuangan: " + e.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE
        );
    }
}

