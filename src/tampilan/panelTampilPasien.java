    private void tabelPasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPasienMouseClicked
        int baris = tabelPasien.getSelectedRow();

    if(baris==-1){
        return;
    }

    String id = tabmode.getValueAt(baris,0).toString();
    String nama = tabmode.getValueAt(baris,1).toString();

    if(panelPembayaran!=null){

        panelPembayaran.setPasienData(id,nama);

        dashboard.showPanel(panelPembayaran);

        return;

    }

    txtIdPasien.setText(id);
    txtNamaPasien.setText(nama);

    String jk = tabmode.getValueAt(baris,2).toString();

    if(jk.equals("Laki-laki")){
        rbLaki.setSelected(true);
    }else{
        rbPerempuan.setSelected(true);
    }

    txtNoHp.setText(tabmode.getValueAt(baris,3).toString());
    txtAlamat.setText(tabmode.getValueAt(baris,4).toString());
    }//GEN-LAST:event_tabelPasienMouseClicked

    private void btncetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncetakActionPerformed
        try {
            // 1. Dapatkan koneksi
            Connection conn = new Koneksi().connect();

            // 2. Tentukan lokasi file .jrxml (Pastikan path ini benar di project Anda)
            String path = "/ireport/pasien.jrxml";
            InputStream reportStream = getClass().getResourceAsStream(path);

            if (reportStream != null) {
                // 3. Kompilasi laporan secara dinamis agar tidak error "InputStream"
                net.sf.jasperreports.engine.JasperReport jr =
                net.sf.jasperreports.engine.JasperCompileManager.compileReport(reportStream);

                // 4. Isi laporan dengan data dari database
                JasperPrint jp = JasperFillManager.fillReport(jr, new HashMap<>(), conn);

                // 5. Tampilkan laporan
                JasperViewer.viewReport(jp, false);
            } else {
                JOptionPane.showMessageDialog(null, "File laporan tidak ditemukan di: " + path);
            }
        } catch (Exception e) {
            // Menampilkan pesan error yang lebih detail di console untuk mempermudah debugging
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal Cetak: " + e.getMessage());
        }
    }//GEN-LAST:event_btncetakActionPerformed


