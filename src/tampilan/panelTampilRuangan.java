    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        int bar = tabelruangan.getSelectedRow();
        if (bar == -1) {
            JOptionPane.showMessageDialog(null, "pilih data ruangan yang ingin diubah");
            return;
        }
        panelEditRuangan panel = new panelEditRuangan(dashboard);
        panel.setRuangan(
            tabmode.getValueAt(bar, 0).toString(),
            tabmode.getValueAt(bar, 1).toString(),
            tabmode.getValueAt(bar, 2).toString(),
            tabmode.getValueAt(bar, 3).toString(),
            tabmode.getValueAt(bar, 4).toString(),
            tabmode.getValueAt(bar, 5).toString(),
            tabmode.getValueAt(bar, 6).toString()
        );
        dashboard.showPanel(panel);
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        int bar = tabelruangan.getSelectedRow();
        if (bar == -1) {
            JOptionPane.showMessageDialog(null, "pilih data ruangan yang ingin dihapus");
            return;
        }
        String id = tabmode.getValueAt(bar, 0).toString();
        int ok = JOptionPane.showConfirmDialog(null, "hapus data ruangan " + id + " ?", "konfirmasi dialog",
            JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from ruangan where kd_ruangan = ?";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, id);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal dihapus" + e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapusActionPerformed

