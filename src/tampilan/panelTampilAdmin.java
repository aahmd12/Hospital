    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
          int selectedRow = tableAdmin.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this,
                "Silakan pilih salah satu data admin di tabel terlebih dahulu!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    String idAdmin = tableAdmin.getValueAt(selectedRow, 0).toString();
    String namaAdmin = tableAdmin.getValueAt(selectedRow, 1).toString();

    int konfirmasi = JOptionPane.showConfirmDialog(
            this,
            "Apakah anda yakin ingin menghapus data admin: "
            + namaAdmin + " (ID: " + idAdmin + ")?",
            "Konfirmasi Hapus Data",
            JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {

        String sql = "DELETE FROM admin WHERE id_admin = ?";

        Koneksi konek = new Koneksi();

        try (Connection conn = konek.connect();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, idAdmin);

            int hasil = ps.executeUpdate();

            if (hasil > 0) {
                JOptionPane.showMessageDialog(this,
                        "Data admin berhasil dihapus!");

                loadDataAdmin();

            } else {
                JOptionPane.showMessageDialog(this,
                        "Data tidak ditemukan!");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_bhapusActionPerformed

    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        int baris = tableAdmin.getSelectedRow();

        if(baris == -1){
            JOptionPane.showMessageDialog(null,"Pilih data yang akan diubah!");
            return;
        }
        String idAdmin = tableAdmin.getValueAt(baris, 0).toString();
        String sql = "SELECT * FROM admin WHERE id_admin = ?";
        
        try (PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setString(1, idAdmin);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {

            panelEditAdmin panel = new panelEditAdmin(dashboard);

            panel.setData(
                    rs.getString("id_admin"),
                    rs.getString("nama"),
                    rs.getString("email"),
                    rs.getString("password")
            );

            dashboard.showPanel(panel);

        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this,
                "Gagal mengambil data: " + e.getMessage());
    }
    }//GEN-LAST:event_beditActionPerformed

