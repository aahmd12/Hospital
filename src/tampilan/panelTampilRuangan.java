     protected void datatable() {
        Object[] Baris = {"Kode Ruangan", "Nama Ruangan", "Jenis Ruangan", "Lantai", "Kapasitas", "Fasilitas","Status"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelruangan.setModel(tabmode);
        String sql = "select * from ruangan where 1=1";
        if (!kdruangan.getText().isEmpty()) {
            sql += " and kd_ruangan like '%" + kdruangan.getText() + "%'";
        }
        if (!nmruangan.getText().isEmpty()) {
            sql += " and nama_ruangan like '%" + nmruangan.getText() + "%'";
        }
        if (jenis.getSelectedIndex() > 0) {
            sql += " and jenis = '" + jenis.getSelectedItem().toString() + "'";
        }
        if (lantai.getValue() instanceof Integer && (Integer) lantai.getValue() > 0) {
            sql += " and lantai = '" + lantai.getValue().toString() + "'";
        }
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("kd_ruangan"),
                    hasil.getString("nama_ruangan"),
                    hasil.getString("jenis"),
                    hasil.getString("lantai"),
                    hasil.getString("kapasitas"),
                    hasil.getString("fasilitas"),
                    hasil.getString("status")
                });               
            }
            tabelruangan.setModel(tabmode);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil" + e);
        }
    }

