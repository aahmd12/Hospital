    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        int baris = tabeldokter.getSelectedRow();

        if(baris == -1){
            JOptionPane.showMessageDialog(null,"Pilih data yang akan diubah!");
            return;
        }

        panelEditDokter panel = new panelEditDokter(dashboard);
        panel.setData(
            tabmode.getValueAt(baris,0).toString(),
            tabmode.getValueAt(baris,1).toString(),
            tabmode.getValueAt(baris,2).toString(),
            tabmode.getValueAt(baris,3).toString(),
            tabmode.getValueAt(baris,4).toString(),
            tabmode.getValueAt(baris,5).toString()
        );

        dashboard.showPanel(panel);
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        int bar = tabeldokter.getSelectedRow();
        if (bar == -1) {
            JOptionPane.showMessageDialog(null, "pilih data dokter yang ingin dihapus");
            return;
        }
        String id = tabmode.getValueAt(bar, 0).toString();
        int ok = JOptionPane.showConfirmDialog(null, "hapus data dokter " + id + " ?", "konfirmasi dialog",
            JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from dokter where id_dokter = ?";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, id);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal dihapus" + e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapusActionPerformed

    private void bcetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcetakActionPerformed
        try {

            if (conn == null || conn.isClosed()) {
                conn = new Koneksi().connect();
            }

            JasperReport jr = JasperCompileManager.compileReport(
                getClass().getResourceAsStream("/ireport/dokter.jrxml")
            );

            Map<String, Object> param = new HashMap<>();
            JasperPrint jp = JasperFillManager.fillReport(jr, param, conn);
            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal mencetak laporan: " + e.getMessage());
        }
    }//GEN-LAST:event_bcetakActionPerformed


