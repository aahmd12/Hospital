    private void loadDataBayar() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Pembayaran");
    model.addColumn("ID Pasien");
    model.addColumn("Nama Pasien");
    model.addColumn("Metode Pembayaran");
    model.addColumn("Jumlah Bayar");
    model.addColumn("Tanggal Pembayaran");

    try {
        String sql = "SELECT "
                + "p.id_payment, "
                + "p.id_pasien, "
                + "ps.nama_pasien, "
                + "p.metode_pembayaran, "
                + "p.jumlah_bayar, "
                + "p.tanggal_pembayaran "
                + "FROM payment p "
                + "JOIN pasien ps ON p.id_pasien = ps.id_pasien "
                + "ORDER BY p.id_payment ASC";

        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_payment"),
                rs.getString("id_pasien"),
                rs.getString("nama_pasien"),
                rs.getString("metode_pembayaran"),
                rs.getDouble("jumlah_bayar"),
                rs.getTimestamp("tanggal_pembayaran")
            });
        }

        tblPembayaran.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
    
